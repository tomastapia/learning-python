# ITERADORES
idiomas = ["Español", "Chino", "Inglés", "Frances", "Italiano", "Mandarin"]


# Iterador
for idioma in idiomas:
    print(idioma)


# for para imprimir numeros
for numero in range(0, 11, 2): # 1ro: numero inicial, 2do: numero de veces que imprime, (Opcional) 3ro: numero de saltos 
    print(f"Numero {numero}")